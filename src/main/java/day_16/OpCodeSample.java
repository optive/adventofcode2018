package day_16;

public class OpCodeSample {
    private int[] before;
    private int[] after;
    private int opCode;
    private int a;
    private int b;
    private int c;

    public int[] getBefore() {
        return before;
    }

    public void setBefore(int[] before) {
        this.before = before;
    }

    public int[] getAfter() {
        return after;
    }

    public void setAfter(int[] after) {
        this.after = after;
    }

    public int getOpCode() {
        return opCode;
    }

    public void setOpCode(int opCode) {
        this.opCode = opCode;
    }

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public int getB() {
        return b;
    }

    public void setB(int b) {
        this.b = b;
    }

    public int getC() {
        return c;
    }

    public void setC(int c) {
        this.c = c;
    }
}
