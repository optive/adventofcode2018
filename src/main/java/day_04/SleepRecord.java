package day_04;

public class SleepRecord {

    private int guardId;
    private int sleepiestMinute;
    private int count;

    public int getGuardId() {
        return guardId;
    }

    public void setGuardId(int guardId) {
        this.guardId = guardId;
    }

    public int getSleepiestMinute() {
        return sleepiestMinute;
    }

    public void setSleepiestMinute(int sleepiestMinute) {
        this.sleepiestMinute = sleepiestMinute;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }
}
