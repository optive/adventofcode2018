package day_10;

public class Star {
    private int x;
    private int y;
    private int xVelocity;
    private int yVeloctiy;

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getxVelocity() {
        return xVelocity;
    }

    public void setxVelocity(int xVelocity) {
        this.xVelocity = xVelocity;
    }

    public int getyVeloctiy() {
        return yVeloctiy;
    }

    public void setyVeloctiy(int yVeloctiy) {
        this.yVeloctiy = yVeloctiy;
    }
}
